#include<iostream>
using namespace std;
int main()
{

  double num1, num2;

  cout << "Enter two Numbers : ";
  cin >> num1 >> num2;

  // addition of two number
  cout << num1 << "+" << num2 << " = "<< num1+num2 << endl;

  // subtraction of two number
  cout << num1 << "-" << num2 << " = "<< num1-num2 << endl;

  // multiplication of two number
  cout << num1 << "*" << num2 << " = "<< num1*num2 << endl;

  // division of two number
  cout << num1 << "/" << num2 << " = "<< num1/num2 << endl;

  return 0;
}
